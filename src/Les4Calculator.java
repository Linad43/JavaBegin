public class Les4Calculator {
    double a;
    double b;

    public double sum(double a, double b) {
        return a + b;
    }
    public double rasnost(double a, double b) {
        return a - b;
    }
    public void mul(double a, double b) {
        System.out.println(a * b);
    }

    public void del(double a, double b) {
        System.out.println(a / b);
    }
}
